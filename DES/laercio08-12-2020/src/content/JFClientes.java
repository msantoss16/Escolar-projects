    private void tbConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbConsultaMouseClicked
        if(jbConfirmar.isEnabled() == false) {
            Connection con = Conexao.abrirConexao();

            ClienteDAO cd = new ClienteDAO(con);
            int selectedR = tbConsulta.getSelectedRow();
            ClienteBean listaCliente = cd.listarTodos().get(selectedR);
            jtxfId.setText(""+listaCliente.getId());
            jtxfNome.setText(listaCliente.getNome());
            jtxfTelCel.setText(listaCliente.getTelCel());
            jtxfTelCom.setText(listaCliente.getTelCom());
            jtxfTelRes.setText(listaCliente.getTelRes());
            jtxfEmail.setText(listaCliente.getEmail());
        }
        ativarBtns(true);
    }//GEN-LAST:event_tbConsultaMouseClicked

