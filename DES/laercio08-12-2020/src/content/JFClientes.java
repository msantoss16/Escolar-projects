    private void jbRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRemoverActionPerformed
        // Excluindo registros da tabela no bd.
        Connection con = Conexao.abrirConexao();
        
        ClienteBean cb = new ClienteBean();
        ClienteDAO cd = new ClienteDAO(con);
        
        cb.setId(Integer.parseInt(jtxfId.getText()));

        //Cria um vetor de Objetos passando a opção de sim ou não
        Object[] opcoes = {"Sim", "Não"};
        
        int i = JOptionPane.showOptionDialog(
                null,
                "Deseja excluir esse úsuario: " + jtxfNome.getText() + "?",
                "Exclusão",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opcoes,
                opcoes[0]);
        
        if (i == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(null, cd.excluirCliente(cb));
            atualizarDados();
        }
        
        Conexao.fecharConexao(con);
    }//GEN-LAST:event_jbRemoverActionPerformed

    private void jbConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbConfirmarActionPerformed
        Connection con = Conexao.abrirConexao();
        ClienteBean cb = new ClienteBean();
        ClienteDAO cd = new ClienteDAO(con);
        
        cb.setNome(jtxfNome.getText());
        cb.setTelRes(jtxfTelRes.getText());
        cb.setTelCom(jtxfTelCom.getText());
        cb.setTelCel(jtxfTelCel.getText());
        cb.setEmail(jtxfEmail.getText());
        JOptionPane.showMessageDialog(null, cd.inserirCliente(cb));
        
        Conexao.fecharConexao(con);
        atualizarDados();
        limpar();
        jbCancelar.setEnabled(false);
        jbConfirmar.setEnabled(false);
        jbInserir.setEnabled(true);
    }//GEN-LAST:event_jbConfirmarActionPerformed

