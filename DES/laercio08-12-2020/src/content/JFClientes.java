    public void limpar() {
        jtxfId.setText("");
        jtxfNome.setText("");
        jtxfTelCel.setText("");
        jtxfTelCom.setText("");
        jtxfTelRes.setText("");
        jtxfEmail.setText("");
    }
    
    public void atualizarDados() {
        // Pesquisando valores do banco de dados
        Connection con = Conexao.abrirConexao();
        
        ClienteDAO cd = new ClienteDAO(con);
        
        List<ClienteBean> listaCliente = new ArrayList<ClienteBean>();
        
        listaCliente = cd.listarTodos();
        
        DefaultTableModel tbm = (DefaultTableModel) tbConsulta.getModel();
        
        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        
        int i = 0;
        
        for (ClienteBean cb : listaCliente) {
            tbm.addRow(new String[1]);
            
            tbConsulta.setValueAt(cb.getId(), i, 0);
            tbConsulta.setValueAt(cb.getNome(), i, 1);
            tbConsulta.setValueAt(cb.getEmail(), i, 2);
            
            i++;
        }
        
        Conexao.fecharConexao(con);
        

    }
    
    public void ativarBtns(Boolean Enable) {
        if(Enable){
            if (!jtxfId.getText().equals("") && jbConfirmar.isEnabled() == false) {
                jbAlterar.setEnabled(true);
                jbInserir.setEnabled(true);
                jbRemover.setEnabled(true);
            }
        } else {
            jbAlterar.setEnabled(false);
            jbInserir.setEnabled(false);
            jbRemover.setEnabled(false);
        }
    }
    
    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // Alterando valores no banco de dados
        Connection con = Conexao.abrirConexao();
        
        ClienteBean cb = new ClienteBean();
        ClienteDAO cd = new ClienteDAO(con);
        
        cb.setId(Integer.parseInt(jtxfId.getText()));
        cb.setNome(jtxfNome.getText());
        cb.setTelRes(jtxfTelRes.getText());
        cb.setTelCom(jtxfTelCom.getText());
        cb.setTelCel(jtxfTelCel.getText());
        cb.setEmail(jtxfEmail.getText());
       
        JOptionPane.showMessageDialog(null, cd.alterarCliente(cb));
        atualizarDados();
        Conexao.fecharConexao(con);
    }//GEN-LAST:event_jbAlterarActionPerformed

